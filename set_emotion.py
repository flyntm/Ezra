def set_emotion(emotion):

    print(f"👀 Emotion: {emotion}")

    if emotion == "happy":
        print("😊 Eyes widen")

    elif emotion == "thinking":
        print("🤔 Slow blink")

    elif emotion == "curious":
        print("👀 Looking up")

    elif emotion == "confused":
        print("😕 Uneven eyelids")